#Tuple unpacking in Python is a technique that allows you to assign multiple values from a tuple to multiple variables in a single step. This makes your code cleaner and more readable.

# Defining a tuple
person = ("Alice", 25, "Engineer")

# Unpacking the tuple into variables
name, age, profession = person

print(name)       # Output: Alice
print(age)        # Output: 25
print(profession) # Output: Engineer
print(name, age, profession)
print(len(person))
